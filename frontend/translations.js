// ── HELPER FUNCTIONS ─────────────────────────────────────────
let currentLang = 'en';

function setLang(lang){
  currentLang = lang;
  localStorage.setItem('kanoor_lang', lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = (lang === 'ur') ? 'rtl' : 'ltr';
}

function getLang(){
  return localStorage.getItem('kanoor_lang') || 'en';
}

function t(key){
  return (UI_STRINGS[currentLang] && UI_STRINGS[currentLang][key]) || UI_STRINGS.en[key] || key;
}

function placeName(place){
  if(currentLang === 'en') return place.n;
  const tr = PLACE_TRANSLATIONS[place.id];
  return (tr && tr[currentLang]) || place.n;
}

// Make available globally
window.KanoorI18n = { UI_STRINGS, PLACE_TRANSLATIONS, setLang, getLang, t, placeName, get currentLang(){return currentLang} };
